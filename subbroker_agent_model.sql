-- 1. Add 'agent' to user_role ENUM (wrapped in DO block for safety if it already exists)
DO $$ BEGIN
    CREATE TYPE user_role_new AS ENUM ('admin', 'staffmanager', 'rm', 'customer', 'agent');
EXCEPTION
    WHEN duplicate_object THEN null;
END $$;
-- Assuming PostgreSQL 12+, ALTER TYPE ADD VALUE IF NOT EXISTS is cleaner:
ALTER TYPE user_role ADD VALUE IF NOT EXISTS 'agent';


-- 2. AGENT PROFILES TABLE (Extends profiles)
CREATE TABLE IF NOT EXISTS agent_profiles (
    agent_id UUID PRIMARY KEY REFERENCES profiles(id) ON DELETE CASCADE,
    pan_number TEXT,
    aadhar_number TEXT,
    bank_details JSONB DEFAULT '{}'::jsonb, -- e.g., {"account_name": "", "account_number": "", "ifsc": ""}
    kyc_status TEXT DEFAULT 'pending', -- 'pending', 'approved', 'rejected'
    total_earnings NUMERIC DEFAULT 0,
    withdrawn_earnings NUMERIC DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- 3. AGENT SETTINGS TABLE (Granular Config)
CREATE TABLE IF NOT EXISTS agent_settings (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    -- Can be global (agent_id IS NULL, company_id IS NULL)
    -- specific to a company (agent_id IS NULL)
    -- specific to an agent (company_id IS NULL)
    -- specific to an agent + company combo.
    agent_id UUID REFERENCES agent_profiles(agent_id) ON DELETE CASCADE,
    company_id UUID REFERENCES companies(id) ON DELETE CASCADE,
    fixed_markup NUMERIC DEFAULT 5,         -- Platform's fixed cut per share
    margin_threshold NUMERIC DEFAULT 10,    -- Min margin difference before profit sharing activates
    margin_percentage NUMERIC DEFAULT 20,   -- Platform percentage cut when margin > threshold
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE NULLS NOT DISTINCT (agent_id, company_id)
);

-- Seed a default global setting (agent_id = NULL, company_id = NULL) if empty
INSERT INTO agent_settings (agent_id, company_id, fixed_markup, margin_threshold, margin_percentage)
SELECT NULL, NULL, 5, 10, 20
WHERE NOT EXISTS (SELECT 1 FROM agent_settings WHERE agent_id IS NULL AND company_id IS NULL);

-- 4. AGENT CLIENT ORDERS (The custom links generated by agents)
CREATE TABLE IF NOT EXISTS agent_client_orders (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    agent_id UUID REFERENCES agent_profiles(agent_id) ON DELETE CASCADE,
    company_id UUID REFERENCES companies(id) ON DELETE CASCADE,
    client_name TEXT NOT NULL,
    client_email TEXT NOT NULL,
    client_phone TEXT,
    quantity INTEGER NOT NULL,
    base_price NUMERIC NOT NULL,     -- The platform price at the time of creation
    fixed_markup NUMERIC NOT NULL,   -- The fixed markup at the time of creation
    selling_price NUMERIC NOT NULL,  -- Agent's Custom Price
    status TEXT DEFAULT 'pending',   -- 'pending', 'paid', 'cancelled'
    link_token TEXT UNIQUE NOT NULL DEFAULT encode(gen_random_bytes(16), 'hex'),
    -- Post payment values
    platform_cut NUMERIC DEFAULT 0,
    agent_earnings NUMERIC DEFAULT 0,
    razorpay_order_id TEXT,
    razorpay_payment_id TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

