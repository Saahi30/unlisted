-- Drop existing policies if running multiple times (optional, but good for idempotency)
DROP POLICY IF EXISTS "Agents can view own profile" ON agent_profiles;
DROP POLICY IF EXISTS "Agents can update own profile" ON agent_profiles;
DROP POLICY IF EXISTS "Agents can insert own profile" ON agent_profiles;
DROP POLICY IF EXISTS "Admins can manage agent profiles" ON agent_profiles;
DROP POLICY IF EXISTS "Admins can view agent profiles" ON agent_profiles;

DROP POLICY IF EXISTS "Anyone can view agent settings" ON agent_settings;
DROP POLICY IF EXISTS "Admins can manage agent settings" ON agent_settings;

DROP POLICY IF EXISTS "Agents can manage own orders" ON agent_client_orders;
DROP POLICY IF EXISTS "Anyone can view order by link_token" ON agent_client_orders;
DROP POLICY IF EXISTS "Admins can view all agent orders" ON agent_client_orders;

-- Agent Profiles Policies
CREATE POLICY "Agents can view own profile" ON agent_profiles FOR SELECT USING (auth.uid() = agent_id);
CREATE POLICY "Agents can update own profile" ON agent_profiles FOR UPDATE USING (auth.uid() = agent_id);
CREATE POLICY "Agents can insert own profile" ON agent_profiles FOR INSERT WITH CHECK (auth.uid() = agent_id);
CREATE POLICY "Admins can manage agent profiles" ON agent_profiles FOR ALL USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
);

-- Agent Settings Policies
CREATE POLICY "Anyone can view agent settings" ON agent_settings FOR SELECT USING (true);
CREATE POLICY "Admins can manage agent settings" ON agent_settings FOR ALL USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
);

-- Agent Client Orders Policies
CREATE POLICY "Agents can manage own orders" ON agent_client_orders FOR ALL USING (auth.uid() = agent_id);
-- Allow public viewing if link_token is matched (necessary for checkout page)
-- Without auth.uid(), this is for anonymous users visiting the checkout link. 
CREATE POLICY "Anyone can view order" ON agent_client_orders FOR SELECT USING (true); 
CREATE POLICY "Admins can view all agent orders" ON agent_client_orders FOR SELECT USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
);
CREATE POLICY "Admins can update all agent orders" ON agent_client_orders FOR UPDATE USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
);
